package com.fordlabs.hungrymoose;

import org.springframework.boot.autoconfigure.condition.ConditionalOnMissingBean;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.ComponentScan;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.ViewResolver;
import org.thymeleaf.spring5.SpringTemplateEngine;
import org.thymeleaf.spring5.view.ThymeleafViewResolver;
import org.thymeleaf.templateresolver.ClassLoaderTemplateResolver;

import javax.annotation.PostConstruct;

@Configuration
@ComponentScan(basePackageClasses = DocsController.class)
public class HungryMooseDocsConfiguration {

    private final SpringTemplateEngine templateEngine;

    public HungryMooseDocsConfiguration(SpringTemplateEngine templateEngine) {
        this.templateEngine = templateEngine;
    }

    @Bean
    @ConditionalOnMissingBean
    public ViewResolver viewResolver(SpringTemplateEngine templateEngine) {
        ThymeleafViewResolver viewResolver = new ThymeleafViewResolver();
        viewResolver.setTemplateEngine(templateEngine);
        return viewResolver;
    }

    @PostConstruct
    public void extension() {
        var templateResolver = new ClassLoaderTemplateResolver();

        templateResolver.setPrefix("templates/fragments/");
        templateResolver.setCacheable(false);
        templateResolver.setSuffix(".html");
        templateResolver.setTemplateMode("HTML5");
        templateResolver.setCharacterEncoding("UTF-8");

        templateEngine.addTemplateResolver(templateResolver);
    }
}
